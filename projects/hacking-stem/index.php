				<h1>Our audience, teachers, are on tight budgets. We designed for affordability.</h1>
				<p>The principle of our solution needed to fulfill the following.</p>
				<ol>
					<li><b>Modularity</b> The needs of classrooms vary. Some classrooms need both materials and tools to complete the lessons. Others may only need the materials. Designing a packaging system that would be adaptable would make manufacturing cost lower.</li>
					<li><b>Size</b> Smaller the kit, the cheaper the cost to ship.</li>
					<li><b>Material</b> The material should be cheap, however sturdy enough to survive long distances.</li>
				</ol>
