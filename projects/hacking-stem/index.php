            	 <h1>Project brief.</h1>                 
            	 <p>The goal of Microsoft's Hacking STEM team is to be a resource for teachers. We delivered free lesson plans accessible online, designed to be affordable—utilizing accessible, everyday materials—compliant with middle school science, technology, engineering, and math standards. However, how do we accommodate students in India, United Arab Emirates, or China where many the easily-accessible materials we order on Amazon.com are not as accessible? Hacking STEM Lesson Plan Kits were designed to be complete lesson plan kits, fully stocked with the materials and tools for a student to complete a lesson plan at a price of $5 or less.</p>
