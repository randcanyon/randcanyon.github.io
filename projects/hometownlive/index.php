				<h1>Project brief and my role.</h1>
				<p>When I started at Sinclair Broadcast Group, they had just over 200 local news properties across the United States, each having their own individual news and weather apps managed through many different content management systems. For our company, it was a disjointed creation and consumption experience that didn't work for anyone's benefit--our users or our business. Hometown Live was conceived as is a multiplatform news app that aggregated news stories for users based simply on their location, pulling from all our stations. Our goal was to save millions of dollars by providing a single publishing platform for our creators and a single destination for our users.</p>
				<p>Working with senior designer, Jonathan Kersten, and art director, Jim Sharon, I was a UX and visual designer for Hometown Live. Collaborating designers, David Buitrago, Kevin Gangi, Megan Harris, and Mykhaylo Sinenko, on branding and consulting with our partners at Ratio Design on technical challenges, we finished the designs by the time I left Sinclair Broadcast Group in September of 2015. Downloadable at a later date at <a href="http://hometownlive.com/">HometownLive.com</a>.</p>
