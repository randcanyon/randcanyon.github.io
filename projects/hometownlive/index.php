				<h1>Framing our problem and goal.</h1>
				<p>At its core, the question we needed to answer was how can we get more eyes on our content? Meaning (1) how do we reach more people with our content and (2) how do we get people who already follow our content to consume more of it? </p>
				<ol>
					<li>Our audience was becoming more mobile, consuming content on the go. We needed to tap into this market to empower our current readers and reach new readers.</li>
					<li>Social media, especially Facebook, is an undeniable driving force in viewership. We needed leverage social if we’re going to reach more people.</li>
					<li>Interesting stories are interesting stories and shouldn’t be prescribed by geographic location. Taking full advantage of our more than 200 stations nationwide, we can deliver original stories that no one else can.</li>
				</ol>
				<p>Our solution was to create a Sinclair news mobile app, focusing on original content and leveraging opportunities for social experiences.</p>
			</div>

