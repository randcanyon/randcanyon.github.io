			<div class="text">
				<h1>Leveraging social.</h1>
				<p>How can social (1) get our users to read a story, (2) share a story, and (3) interact with a story?</p>
				<ol>
					<li>Social proof stories. Let users know this is a story that they might want to read because either (1) their friends read and reacted to it or (2) many people read and reacted to it.</li>
					<li>Enable sharing opportunities at every level. Stories shared to social media either (1) open the story in app if the user has Hometown Live installed or (2) link to  web viewable versions for mobile or desktop.</li>
					<li>Reactions to stories are easy, frictionless ways for users to engage and contribute to a story. It will also inform readers what they should expect.</li>
				</ol>
			</div>

